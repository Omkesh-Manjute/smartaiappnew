const extractJsonObject = (text: string): any => {
  const cleaned = text.replace(/```json|```/gi, '').trim();
  const match = cleaned.match(/\{[\s\S]*\}/);
  if (!match) {
    throw new Error('Could not parse JSON object from AI response');
  }
  return JSON.parse(match[0]);
};

export interface TutorMessage {
  role: 'user' | 'model';
  content: string;
}

export type TutorMode = 'simple' | 'teacher' | 'exam' | 'quiz';

const callGroq = async (prompt: string, messages: TutorMessage[], settings: any): Promise<string> => {
  const apiKey = settings.groq_api_key || import.meta.env.VITE_GROQ_API_KEY;
  if (!apiKey) throw new Error('Groq API Key not configured');

  const response = await fetch('https://api.groq.com/openai/v1/chat/completions', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'Authorization': `Bearer ${apiKey}`,
    },
    body: JSON.stringify({
      model: settings.groq_model || 'llama-3.1-70b-versatile',
      messages: [
        ...messages.map(m => ({ role: m.role === 'model' ? 'assistant' : 'user', content: m.content })),
        { role: 'user', content: prompt }
      ],
      temperature: 0.7,
      max_tokens: 2048,
    }),
  });

  const rawText = await response.text();
  if (!response.ok) throw new Error(`Groq API Error: ${response.status} - ${rawText}`);
  
  const data = JSON.parse(rawText);
  return data.choices?.[0]?.message?.content || '';
};

const callIntelligence = async (prompt: string, previousMessages: TutorMessage[] = []): Promise<string> => {
  const settings = await SystemSettingsService.getSettings();
  
  if (settings.ai_provider === 'groq') {
    return callGroq(prompt, previousMessages, settings);
  }
  
  const contents = [
    ...previousMessages.map(m => ({
      role: m.role === 'model' ? 'model' : 'user',
      parts: [{ text: m.content }]
    })),
    { role: 'user', parts: [{ text: prompt }] }
  ];

  return callGemini(contents, { temperature: 0.7, maxOutputTokens: 2048 }, settings);
};

const callGemini = async (
  contents: Array<{ role?: string; parts: Array<{ text: string }> }>,
  generationConfig: { temperature: number; maxOutputTokens: number },
  settings?: any
): Promise<string> => {
  const apiKey = settings?.gemini_api_key || GEMINI_API_KEY;
  const modelName = settings?.gemini_model || GEMINI_MODEL;
  
  if (!apiKey) {
    throw new Error('Gemini API key is not configured');
  }

  const url = `https://generativelanguage.googleapis.com/v1beta/models/${modelName}:generateContent?key=${apiKey}`;

  const response = await fetch(url, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({ contents, generationConfig }),
  });

  const raw = await response.text();
  let data: any = {};

  if (raw) {
    try {
      data = JSON.parse(raw);
    } catch {
      throw new Error('Gemini returned invalid JSON response');
    }
  }

  if (!response.ok) {
    const message =
      data?.error?.message || `Gemini API error: ${response.status}`;
    throw new Error(message);
  }

  const text = data?.candidates?.[0]?.content?.parts?.[0]?.text;
  if (!text) {
    throw new Error('No content generated by Gemini');
  }

  return String(text).trim();
};

// Generate test questions using Gemini Flash API
export const generateTestQuestions = async (
  params: TestGenerationParams
): Promise<GeneratedQuestion[]> => {
  try {
    const prompt = `
You are an expert educational content creator. Generate ${params.numQuestions} multiple choice questions for the following:

Subject: ${params.subject}
Chapter: ${params.chapter}
Topic: ${params.topic}
Difficulty Level: ${params.difficulty}

For each question, provide:
1. A clear, concise question
2. Exactly 4 answer options (A, B, C, D)
3. The correct answer index (0 for A, 1 for B, 2 for C, 3 for D)
4. A brief explanation of why the answer is correct
5. Difficulty level (easy, medium, or hard)

Format your response as a valid JSON array with this exact structure:
[
  {
    "question": "Question text here?",
    "options": ["Option A", "Option B", "Option C", "Option D"],
    "correctAnswer": 0,
    "explanation": "Explanation of the correct answer",
    "difficulty": "easy"
  }
]

Important: Return ONLY the JSON array, no markdown formatting, no additional text.
`;

    const text = await callGemini(
      [
        {
          parts: [{ text: prompt }],
        },
      ],
      {
        temperature: 0.7,
        maxOutputTokens: 8192,
      }
    );

    // Extract JSON from the response
    const jsonMatch = text.match(/\[[\s\S]*\]/);
    if (!jsonMatch) {
      throw new Error('Could not parse generated questions');
    }

    const questions: GeneratedQuestion[] = JSON.parse(jsonMatch[0]);

    // Validate and sanitize questions
    return questions.map((q, index) => ({
      question: q.question || `Question ${index + 1}`,
      options: q.options?.length === 4 ? q.options : ['Option A', 'Option B', 'Option C', 'Option D'],
      correctAnswer: typeof q.correctAnswer === 'number' && q.correctAnswer >= 0 && q.correctAnswer <= 3
        ? q.correctAnswer
        : 0,
      explanation: q.explanation || 'No explanation provided',
      difficulty: ['easy', 'medium', 'hard'].includes(q.difficulty) ? q.difficulty : 'medium',
    }));
  } catch (error) {
    console.error('Error generating test questions:', error);
    // Return fallback questions if API fails
    return generateFallbackQuestions(params.numQuestions);
  }
};

