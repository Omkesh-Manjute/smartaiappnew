export const generateSubjectBlueprint = async (
  params: { prompt: string; sourceText?: string }
): Promise<SubjectBlueprint> => {
  const normalizeBlueprint = (parsed: SubjectBlueprint): SubjectBlueprint => {
    if (!parsed.subjects || !Array.isArray(parsed.subjects) || parsed.subjects.length === 0) {
      throw new Error('Blueprint did not contain subjects');
    }
    return {
      subjects: parsed.subjects.map((subject, subjectIndex) => ({
        name: subject.name || `Subject ${subjectIndex + 1}`,
        description: subject.description || 'Generated subject',
        grade: typeof subject.grade === 'number' ? Math.min(12, Math.max(1, subject.grade)) : 10,
        chapters: (subject.chapters || []).map((chapter, chapterIndex) => ({
          name: chapter.name || `Chapter ${chapterIndex + 1}`,
          description: chapter.description || 'Generated chapter',
          content: chapter.content || chapter.description || 'Generated content',
        })),
      })),
    };
  };

  try {
    // Prefer server endpoint so deployment secrets can be used even without VITE_GEMINI_API_KEY.
    try {
      const response = await fetch('/api/ai/subject-blueprint', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          prompt: params.prompt,
          sourceText: params.sourceText,
        }),
      });

      if (response.ok) {
        const raw = await response.text();
        const data = raw ? JSON.parse(raw) : {};
        const parsed = extractJsonObject(String(data?.text || ''));
        return normalizeBlueprint(parsed as SubjectBlueprint);
      }
    } catch (serverError) {
      console.warn('Server AI subject generation failed, falling back to client Gemini:', serverError);
    }

    const prompt = `
You are an academic curriculum designer.

Teacher/Admin instruction:
${params.prompt}

Reference text extracted from uploaded material:
${params.sourceText?.slice(0, 12000) || 'No extra source text provided.'}

Generate a JSON object in this exact structure:
{
  "subjects": [
    {
      "name": "Subject Name",
      "description": "Short description",
      "grade": 10,
      "chapters": [
        {
          "name": "Chapter Name",
          "description": "Chapter description",
          "content": "High-level summary content"
        }
      ]
    }
  ]
}

Rules:
- 1 to 5 subjects
- Each subject should have 3 to 12 chapters
- Return JSON only, no markdown
`;

    const text = await callGemini(
      [{ parts: [{ text: prompt }] }],
      {
        temperature: 0.5,
        maxOutputTokens: 8192,
      }
    );

    const parsed = extractJsonObject(text) as SubjectBlueprint;
    return normalizeBlueprint(parsed);
  } catch (error) {
    console.error('Error generating subject blueprint:', error);
    return {
      subjects: [
        {
          name: 'Generated Subject',
          description: 'Auto-generated subject outline',
          grade: 10,
          chapters: [
            {
              name: 'Chapter 1',
              description: 'Auto-generated chapter',
              content: params.prompt || 'Generated content',
            },
          ],
        },
      ],
    };
  }
};

// AI Tutor - Get response from Gemini
const getModeSystemPrompt = (mode: TutorMode): string => {
  const commonInstructions = `
Language Rules: 
- If providing Hindi examples, provide the Hindi script (Devanagari) first, then the English translation.
- IMPORTANT: DO NOT provide Romanized/Phonetic Hindi in brackets (e.g. avoid "(Main kahan ja raha hoon)") as it confuses the speech system.

CRITICAL FORMATTING RULES:
You MUST structure your response EXACTLY using these three Markdown headers:
## Explanation
(Provide the main explanation, answer, or quiz question here)

## Example
(Provide a concrete example or analogy here)

## Key Points
(Provide 1-3 short bullet points summarizing the takeaway or hint)

Do not add any conversational filler before the first header.
`;

  switch (mode) {
    case 'simple':
      return `You are a friendly AI tutor. ${commonInstructions} Explain concepts in the simplest possible language. Use short sentences, everyday analogies, and avoid jargon. Use bullet points for key facts and give one relatable example. Keep the response concise (under 200 words).`;
    case 'teacher':
      return `You are an experienced school teacher. ${commonInstructions} Give structured, detailed explanations. Start with a brief introduction, use numbered steps or bullet points, provide examples, and end with a summary or key takeaway. Use diagrams descriptions where helpful.`;
    case 'exam':
      return `You are an exam preparation expert. ${commonInstructions} Focus on key points likely to appear in exams. Use bullet points for important concepts, highlight formulas or definitions with emphasis, mention common mistakes, and provide 1-2 typical exam-style practice questions at the end.`;
    case 'quiz':
      return `You are a quiz master. ${commonInstructions} Instead of directly explaining, ask the student a question about the topic to test their understanding. If they answer correctly, congratulate them and ask a harder follow-up. If they answer incorrectly, gently explain the correct answer and ask an easier question. Always be encouraging.`;
    default:
      return `You are a helpful and knowledgeable AI tutor. ${commonInstructions} Provide clear, well-structured explanations with examples.`;
  }
};

export const getTutorResponse = async (
  message: string,
  context?: {
    subject?: string;
    chapter?: string;
    previousMessages?: TutorMessage[];
    mode?: TutorMode;
  }
): Promise<string> => {
  try {
    // Try server endpoint first
    try {
      const response = await fetch('/api/ai/tutor', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ message, context }),
      });

      if (response.ok) {
        const raw = await response.text();
        const data = raw ? JSON.parse(raw) : {};
        const text = typeof data?.text === 'string' ? data.text : '';
        if (text.trim()) return text.trim();
      }
    } catch {
      // Server endpoint failed, fall through to client-side Gemini
    }

    // Client-side AI call with mode-specific prompt
    const mode = context?.mode || 'teacher';
    const systemPrompt = getModeSystemPrompt(mode);

    const contextParts = [];
    if (context?.subject) contextParts.push(`Subject: ${context.subject}`);
    if (context?.chapter) contextParts.push(`Chapter: ${context.chapter}`);

    const prompt = `${systemPrompt}\n\n${contextParts.length ? contextParts.join('\n') + '\n\n' : ''}Student's question: ${message}`;
    const previousMessages = context?.previousMessages?.slice(-5) || [];

    const text = await callIntelligence(prompt, previousMessages);
    return text;
  } catch (error) {
    console.error('Error getting tutor response:', error);
    return getFallbackTutorResponse(message);
  }
};

